function dateCheck(input) {
    var matches = /^(\d\d?)[-\/](\d\d?)[-\/](\d{4})$/.exec(input);
    if (matches === null) {
        return false;
    }
    var d = matches[2];
    var m = matches[1] - 1;
    var y = matches[3];
    birthDate = new Date(y, m, d);
    // == is used to test equality here since numbers and strings are being compared
    return (birthDate.getDate() == d &&
        birthDate.getMonth() == m &&
        birthDate.getFullYear() == y) 
}

function listBirthdays(birthDate) {
    // compile a list of all birthdays up to current date
    var today = new Date();
    var birthDay = birthDate;
    var yearsOld = preBillBoard(birthDate);
    birthDays = [];
    birthDay.setFullYear(birthDay.getFullYear() + yearsOld);
    while (birthDay <= today) {
       saturday = nearestSaturday(birthDay);
       birthDays.push([yearsOld, saturday]);
       // account for birthdays that fall on 2-29th 
       if (birthDay.getMonth() === 1 && birthDay.getDate() === 29) {
           birthDay = new Date(birthDay.getFullYear() + 4, birthDay.getMonth(), birthDay.getDate());
       }
       else {
           birthDay = new Date(birthDay.getFullYear() + 1, birthDay.getMonth(), birthDay.getDate());
       }
       yearsOld ++
    }
    compileDisplay(birthDays);    
}

function preBillBoard() {
    // billboard started charting top songs in August, 1958
    if (birthDate < firstBillboard) {
        return Math.abs(birthDate.getFullYear() - 1959)
    }
    else {
        return 0
    }
}
        
function nearestSaturday(birthDay) {
    // for each birthday find date of nearest Saturday and add to birthDays array
    var diffFromSaturday = 6 - birthDay.getDay();
    newDate = new Date(birthDay.getFullYear(), birthDay.getMonth(), birthDay.getDate() + diffFromSaturday);
    newDate = (newDate.getMonth() + 1) + "-" + newDate.getDate() + "-" + newDate.getFullYear();
    return newDate;
}

